export type Dict = typeof vi;

export function t<K extends string>(key: K, ...args: any[]): string {
  const path = key.split('.');
  let cur: any = vi;
  for (const p of path) {
    if (cur == null) return key;
    cur = cur[p];
  }
  if (typeof cur === 'function') return cur(...args);
  if (typeof cur === 'string') return cur;
  return key;
}
